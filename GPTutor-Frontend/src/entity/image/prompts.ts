function getRandomElementByIndex(array: string[]) {
  const randomIndex = Math.floor(Math.random() * array.length);
  return array[randomIndex];
}

function getRandomElementsByIndex(array: string[], numElements: number) {
  const randomElements: string[] = [];

  while (randomElements.length < numElements) {
    const randomElement = getRandomElementByIndex(array);
    if (!randomElements.includes(randomElement)) {
      randomElements.push(randomElement);
    }
  }

  return randomElements;
}

